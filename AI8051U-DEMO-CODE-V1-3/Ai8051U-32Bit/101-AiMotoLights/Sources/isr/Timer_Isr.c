//========================================================================
//                            全局时间戳（1ms时基）
//========================================================================
// 注意：在app_intKey.c和app_vehicle_signal.c中声明为extern，这里定义并递增
// 摩托车灯组系统所有模块都使用这个时间戳
volatile u32 system_tick_ms = 0;

//========================================================================
// 函数: Timer0_ISR_Handler
// 描述: Timer0中断函数.
// 参数: none.
// 返回: none.
// 版本: V1.0, 2020-09-23
// 优化: V1.1, 2025-10-21 - 添加中断嵌套保护
//========================================================================
void Timer0_ISR_Handler (void) interrupt TMR0_VECTOR		//进中断时已经清除标志
{
    //========================================================================
    // 优化2：禁止中断嵌套（保证ISR执行时间可控）
    // 执行时间：~25μs @ 48MHz（或 ~50μs @ 24MHz）
    // 期间其他中断会被延迟，但硬件外设（I2C/SPI/ADC/PWM）仍正常工作
    //========================================================================
    EA = 0;  // 禁止全局中断，防止中断嵌套
    
    //========================================================================
    // 全局1ms时基递增（用于状态机延时、按键事件、信号检测等）
    // 开销：<0.5μs，CPU占用增加<0.5%
    //========================================================================
    system_tick_ms++;  // 全局毫秒计数器递增（摩托车灯组系统必需）
    
    Task_Marks_Handler_Callback();	//任务标记回调函数
    
    EA = 1;  // 恢复全局中断
    //========================================================================
}

