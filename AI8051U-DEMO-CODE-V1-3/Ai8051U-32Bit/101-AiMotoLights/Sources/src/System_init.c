//========================================================================
//                                IO口配置
//========================================================================
void GPIO_config(void)
{
    //========================================================================
    // 摩托车智能联动灯组系统GPIO配置（更新：SPI1-DMA+硬件PWM）
    //========================================================================
    
    // P0口：原车信号输入 + ADC输入
    // P0.0-P0.5: 数字输入（刹车、雾灯、近光、远光、左转、右转）
    // P0.6-P0.7: ADC输入（音频、电池电压）
    P0M1 = 0xC0;   P0M0 = 0x00;   // P0.6,P0.7高阻输入(ADC), 其他准双向口
    
    // P1口：PWM输出 + 状态LED + 蜂鸣器
    // P1.0-P1.3: 软件PWM输出（刹车灯、RGB三色）
    // P1.5: 蜂鸣器控制（软件PWM）
    // P1.6: 错误LED(红色)
    // P1.7: 状态LED(蓝色)
    // 注意：使用软件PWM，不需要硬件PWM配置
    P1M1 = 0x00;   P1M0 = 0xC0;   // P1.6,P1.7推挽输出（状态LED）
    // P1.0-P1.3, P1.5 为准双向口，软件PWM控制
    
    // P2口：WS2812B (SPI1接口)
    // P2.4: SS (不使用)
    // P2.5: MOSI (WS2812B数据输出)
    // P2.6: MISO (SPI占用，高阻+下拉)
    // P2.7: SCLK (SPI时钟)
    // 注意：SPI1配置由WS2812B模块初始化时完成
    P2M1 = 0x00;   P2M0 = 0x00;   // 先设为准双向口，SPI模块会重新配置
    
    // P3口：按键输入（外部中断）
    // P3.2: 亮度按键(INT3), P3.6: 测试按键(INT2), P3.7: 菜单按键(INT0)
    P3M1 = 0x00;   P3M0 = 0x00;   // 准双向口（按键输入，内部上拉）
    
    // P4口：系统控制
    // P4.6: 掉电检测, P4.7: 复位按键
    P4M1 = 0xC0;   P4M0 = 0x00;   // P4.6,P4.7高阻输入
    
    // P5口：UART调试（UART4）
    P5M1 = 0x00;   P5M0 = 0x02;   // P5.1推挽输出
    
    // P6、P7口：保持默认
    P6M1 = 0x00;   P6M0 = 0x00;
    P7M1 = 0x00;   P7M0 = 0x00;
    
    // 初始化输出引脚状态
    // 注意：PWM引脚由PWMA/PWMB模块控制，不需要手动设置初值
    P16 = 0;  // 错误LED关
    P17 = 0;  // 状态LED关
    
    // 使能扩展寄存器访问（用于GPIO高级配置）
    EAXFR = 1;
    
    // 配置GPIO上拉（按键）
    P3PU |= 0xC4;  // P3.2,P3.6,P3.7使能上拉（按键输入）
    
    printf("GPIO configured (SPI1-DMA@P2 + Hardware PWM@P1)\r\n");
}

//========================================================================
//                               定时器配置
//========================================================================
void Timer_config(void)
{
    AUXR = 0x80;    //Timer0 set as 1T, 16 bits timer auto-reload, 
    TH0 = (u8)(Timer0_Reload / 256);
    TL0 = (u8)(Timer0_Reload % 256);
    ET0 = 1;        //Timer0 interrupt enable
    TR0 = 1;        //Tiner0 run
}

//========================================================================
//                              ADC初始化
//========================================================================
void ADC_config(void)
{
    ADCTIM = 0x3f;      //设置 ADC 内部时序，ADC采样时间建议设最大值
    ADCCFG = 0x2f;      //设置 ADC 时钟为系统时钟/2/16/16
    ADC_CONTR = 0x80;   //使能 ADC 模块
}

//========================================================================
//                              PWM初始化
//========================================================================
void PWM_config(void)
{
    PWMB_CCER1 = 0x00;  //写 CCMRx 前必须先清零 CCxE 关闭通道
    PWMB_CCMR1 = 0x30;  //通道模式配置，翻转模式
    PWMB_CCER1 = 0x01;  //配置通道输出使能和极性

    PWMB_ARRH = 0x07;   //设置周期时间
    PWMB_ARRL = 0xff;

    PWMB_ENO = 0x00;    //PWM5关闭输出
//    PWMB_ENO = 0x01;    //PWM5使能输出
    PWMB_PS = 0x03;     //高级 PWM 通道输出脚选择位, P50

    PWMB_BKR = 0x80;    //使能主输出
    PWMB_CR1 |= 0x01;   //开始计时
}

//========================================================================
//                              UART初始化
//========================================================================
void UartInit(void)
{
#if(PrintUart == 1)
    S1_S1 = 0;      //UART1 switch to, 00: P3.0 P3.1, 01: P3.6 P3.7, 10: P1.6 P1.7, 11: P4.3 P4.4
    S1_S0 = 0;
    SCON = (SCON & 0x3f) | 0x40;
    T1x12 = 1;      //定时器时钟1T模式
    S1BRT = 0;      //串口1选择定时器1为波特率发生器
    TL1  = TM;
    TH1  = TM>>8;
    TR1 = 1;        //定时器1开始计时

//    SCON = (SCON & 0x3f) | 0x40; 
//    T2L  = TM;
//    T2H  = TM>>8;
//    AUXR |= 0x15;   //串口1选择定时器2为波特率发生器
#else
    S2_S = 1;       //UART2 switch to: 0: P1.2 P1.3,  1: P4.2 P4.3
    S2CFG |= 0x01;  //使用串口2时，W1位必需设置为1，否则可能会产生不可预期的错误
    S2CON = (SCON & 0x3f) | 0x40; 
    T2L  = TM;
    T2H  = TM>>8;
    AUXR |= 0x14;   //定时器2时钟1T模式,开始计时
#endif
}

void UartPutc(unsigned char dat)
{
#if(PrintUart == 1)
    SBUF = dat; 
    while(TI == 0);
    TI = 0;
#else
    S2BUF  = dat; 
    while(S2TI == 0);
    S2TI = 0;    //Clear Tx flag
#endif
}

char putchar(char c)
{
    UartPutc(c);
    return c;
}

//========================================================================
//                                系统初始化
//========================================================================
void SYS_Init(void)
{
    GPIO_config();
    Timer_config();
    ADC_config();
    PWM_config();
    UartInit();
    
    //========================================================================
    // 优化3：设置中断优先级（保护任务调度中断）
    //========================================================================
    // AI8051U支持4级中断优先级（IPH, IP组合）：
    //   优先级3（最高）: IPH=1, IP=1
    //   优先级2:         IPH=1, IP=0
    //   优先级1:         IPH=0, IP=1
    //   优先级0（最低）: IPH=0, IP=0（默认）
    //
    // 优先级规划：
    //   - Timer0（任务调度）: 优先级1（高）⭐ 最关键
    //   - INT0-3（按键中断）: 优先级0（低，默认）
    //   - Timer1（波特率）:   优先级0（低，无中断）
    //   - UART（串口）:       优先级0（低，轮询方式）
    //
    // 效果：
    //   - Timer0永不被其他中断打断，保证任务调度时序精确
    //   - 即使将来添加新中断，也不会影响任务调度
    //   - 与EA=0形成双重保护机制
    //========================================================================
    
    PT0 = 1;  // Timer0中断优先级 = 1（高于默认的0）⭐ 关键设置
    PT1 = 0;  // Timer1保持默认优先级0（实际不使用中断）
    PS  = 0;  // 串口保持默认优先级0（实际不使用中断）
    
#if ENABLE_INT_KEY
    // 外部中断按键优先级设置为0（低），不影响任务调度
    PX0 = 0;  // INT0优先级0（低）
    PX1 = 0;  // INT1优先级0（低）
    // INT2/3的优先级在IP2寄存器中，保持默认0
#endif
    
    // 注意：其他未使用的中断保持默认优先级0
    //========================================================================
    
    EA = 1;

    APP_config();
}
